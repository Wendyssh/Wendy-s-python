#传值（重新创建了一个空间，两个不同的空间）和传址（空间，指向内存中的同一块内存，会得到新的列表）的区别
#对于简单的数值，采用传值操作，即在函数内对参数的操作不影响外围的变量（相当于复印）
#对于复杂变量，采用传地址操作，此事函数内的参数和外部变量是同一块内容，任何地方对此内容的更改都影响另外的变量或参数的使用
#两个函数几乎一样，为何打印结果不同？
def a(n):
	n[2]=300
	print(n)
	return None

def b(n):
	n+=100
	print(n)
	return None

an=[1,2,3,4,5,6]
bn=9

print(an)
a(an)
print(an)

print(bn)
b(bn)
print(bn)
